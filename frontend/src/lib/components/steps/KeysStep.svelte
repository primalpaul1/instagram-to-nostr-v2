<script lang="ts">
  import { onMount } from 'svelte';
  import { wizard } from '$lib/stores/wizard';
  import { generateKeyPair } from '$lib/nostr';
  import { hexToNpub } from '$lib/nip46';

  let keyPair = $wizard.keyPair;
  let copied = { npub: false, nsec: false };
  let acknowledged = false;
  let downloaded = false;

  function downloadKeys() {
    if (!keyPair) return;

    const content = `Your Nostr Keys
================

PUBLIC KEY (share this):
${keyPair.npub}

SECRET KEY (keep this private!):
${keyPair.nsec}

----------------
Generated by Insta to Primal
https://instatoprimal.com

IMPORTANT: Store this file securely!
Anyone with your secret key can post as you.
`;

    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'nostr-keys.txt';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
    downloaded = true;
  }

  $: isNip46Mode = $wizard.authMode === 'nip46';
  $: nip46Npub = $wizard.nip46Pubkey ? hexToNpub($wizard.nip46Pubkey) : '';

  onMount(() => {
    if (!isNip46Mode && !keyPair) {
      keyPair = generateKeyPair();
      wizard.setKeyPair(keyPair);
    }
  });

  async function copyToClipboard(text: string, type: 'npub' | 'nsec') {
    await navigator.clipboard.writeText(text);
    copied[type] = true;
    setTimeout(() => {
      copied[type] = false;
    }, 2000);
  }

  function handleContinue() {
    if (!isNip46Mode && (!acknowledged || !downloaded)) return;
    wizard.setStep('videos');
  }

  function handleBack() {
    wizard.setStep('handle');
  }
</script>

<div class="keys-step">
  {#if isNip46Mode}
    <div class="hero-section">
      <div class="success-badge">
