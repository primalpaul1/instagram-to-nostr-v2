              <path d="M5 15H4a2 2 0 01-2-2V4a2 2 0 012-2h9a2 2 0 012 2v1"/>
            </svg>
            Copy
          {/if}
        </button>
      </div>
      <code class="pubkey">{nip46Npub}</code>
    </div>

    <div class="info-banner">
