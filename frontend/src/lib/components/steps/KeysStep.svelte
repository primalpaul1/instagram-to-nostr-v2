<script lang="ts">
  import { onMount } from 'svelte';
  import { wizard } from '$lib/stores/wizard';
  import { generateKeyPair } from '$lib/nostr';
  import { hexToNpub } from '$lib/nip46';

  let keyPair = $wizard.keyPair;
  let copied = { npub: false, nsec: false };
  let downloaded = false;

  function downloadKeys() {
    if (!keyPair) return;

    const content = `Your Primal Key
================

This is your login key for Primal. Keep it safe!

PRIMAL KEY:
${keyPair.nsec}

----------------
Generated by Own Your Posts
https://ownyourposts.com

HOW TO USE:
1. Download the Primal app (iOS or Android)
2. Tap "Login" then "Use login key"
3. Paste your Primal Key above

IMPORTANT: This key is like a password.
Anyone with it can access your account.
Store it somewhere safe!
`;

    const blob = new Blob([content], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'primal-key.txt';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
    downloaded = true;
  }

  $: isNip46Mode = $wizard.authMode === 'nip46';
  $: nip46Npub = $wizard.nip46Pubkey ? hexToNpub($wizard.nip46Pubkey) : '';

  onMount(() => {
    console.log('KeysStep mounted, isNip46Mode:', isNip46Mode, 'keyPair:', !!keyPair, 'authMode:', $wizard.authMode);
    if (!isNip46Mode && !keyPair) {
      keyPair = generateKeyPair();
      wizard.setKeyPair(keyPair);
    }
  });

  async function copyToClipboard(text: string, type: 'npub' | 'nsec') {
    await navigator.clipboard.writeText(text);
    copied[type] = true;
    setTimeout(() => {
      copied[type] = false;
    }, 2000);
  }

  function handleContinue() {
    console.log('KeysStep handleContinue called, isNip46Mode:', isNip46Mode, 'downloaded:', downloaded);
    if (!isNip46Mode && !downloaded) {
      console.log('KeysStep: not downloaded yet, returning');
      return;
    }
    console.log('KeysStep: setting step to videos');
    wizard.setStep('videos');
  }

  function handleBack() {
    wizard.setStep('handle');
  }
</script>

<div class="keys-step">
  {#if isNip46Mode}
    <div class="hero-section">
      <div class="success-badge">
