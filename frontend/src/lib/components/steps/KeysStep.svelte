                  <path d="M5 15H4a2 2 0 01-2-2V4a2 2 0 012-2h9a2 2 0 012 2v1"/>
                </svg>
              {/if}
            </button>
          </div>
          <code class="key-value">{keyPair.nsec}</code>
          <span class="key-hint warning">Never share this with anyone!</span>
        </div>
      </div>

      <button class="download-btn" on:click={downloadKeys}>
        <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
          <path d="M21 15v4a2 2 0 01-2 2H5a2 2 0 01-2-2v-4"/>
          <polyline points="7 10 12 15 17 10"/>
          <line x1="12" y1="15" x2="12" y2="3"/>
        </svg>
        {#if downloaded}
          Downloaded!
        {:else}
          Download Keys File
        {/if}
      </button>

      <div class="warning-banner">
        <svg class="warning-icon" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
          <path d="M10.29 3.86L1.82 18a2 2 0 001.71 3h16.94a2 2 0 001.71-3L13.71 3.86a2 2 0 00-3.42 0z"/>
          <line x1="12" y1="9" x2="12" y2="13"/>
          <line x1="12" y1="17" x2="12.01" y2="17"/>
        </svg>
        <p>Your secret key is like a password. Anyone with it can post as you. Store it in a password manager.</p>
      </div>

      <label class="acknowledge-box" class:disabled={!downloaded}>
        <div class="checkbox-wrapper">
          <input type="checkbox" bind:checked={acknowledged} disabled={!downloaded} />
          <div class="custom-checkbox">
            {#if acknowledged}
              <svg width="12" height="12" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3">
                <path d="M20 6L9 17l-5-5"/>
              </svg>
            {/if}
          </div>
        </div>
        <span>I have downloaded and saved my keys securely</span>
      </label>

      <div class="actions">
        <button class="secondary-btn" on:click={handleBack}>
          <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
            <path d="M19 12H5M12 19l-7-7 7-7"/>
          </svg>
          Back
        </button>
        <button class="primary-btn" disabled={!downloaded || !acknowledged} on:click={handleContinue}>
          Select Videos
          <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
            <path d="M5 12h14M12 5l7 7-7 7"/>
          </svg>
        </button>
      </div>
    {/if}
  {/if}
</div>

